    /**
     * Parses font elements to display text using SpriteFont.
     * @returns {void}
     */
    static parseFont() {
        /** @type {NodeListOf<HTMLElement>} */
        const elements = document.querySelectorAll(".font-pixel-huge");

        elements.forEach((el) => {
            new window.SpriteFont(el, "Font Pixel Huge");
        });
    }

    // ###    ##   #  #   ##                #                 #    #                    #           #
    // #  #  #  #  ####  #  #               #                 #    #                    #           #
